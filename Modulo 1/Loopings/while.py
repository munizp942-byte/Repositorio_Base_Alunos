def exercicio_514():
    s = 0
    quantidade = 0
    while True:
        v = int(input("digite um numero ou 0 para sair: "))
        if v == 0:
            break
        s = s + v
        quantidade + 1 
    print("a soma é: ", s)
    print("a quantidade é ", quantidade)
    print("a média é: ", s / quantidade)
exercicio_514()
 